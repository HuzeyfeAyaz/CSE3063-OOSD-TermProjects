package OOP_Project;

/*
Class for the label object to store them in our array list in the dataset
*/

public class Label {
    private int id;
    private String name;


    // Getters

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }


    // Setters

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
